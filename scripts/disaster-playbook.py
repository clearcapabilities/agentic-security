# ─────────────────────────────────────────────────────────────────────────────
# Stack detection
# ─────────────────────────────────────────────────────────────────────────────

def detect_stack(cwd: Path) -> set[str]:
    stack = set()
    # File markers
    if (cwd / "vercel.json").exists() or (cwd / ".vercel").exists():
        stack.add("vercel")
    if (cwd / "fly.toml").exists():
        stack.add("fly")
    if (cwd / "supabase" / "config.toml").exists() or any(cwd.rglob("supabase/migrations/*.sql")):
        stack.add("supabase")
    # Package.json deps
    pkg = cwd / "package.json"
    if pkg.exists():
        try:
            data = json.loads(pkg.read_text())
            deps = {**data.get("dependencies", {}), **data.get("devDependencies", {})}
            if "stripe" in deps or "@stripe/stripe-js" in deps:
                stack.add("stripe")
            if "@supabase/supabase-js" in deps or "@supabase/ssr" in deps:
                stack.add("supabase")
            if "@clerk/nextjs" in deps or "@clerk/clerk-react" in deps or "@clerk/clerk-sdk-node" in deps:
                stack.add("clerk")
            if "@auth0/nextjs-auth0" in deps or "auth0" in deps:
                stack.add("auth0")
            if "aws-sdk" in deps or "@aws-sdk/client-s3" in deps:
                stack.add("aws")
            stack.add("npm")  # always include npm advice if package.json present
        except Exception:
            pass
    # Env file hints
    for env_name in (".env", ".env.local", ".env.production", ".env.example"):
        ep = cwd / env_name
        if ep.exists():
            try:
                content = ep.read_text(errors="ignore")
                if "STRIPE_" in content: stack.add("stripe")
                if "SUPABASE_" in content: stack.add("supabase")
                if "CLERK_" in content: stack.add("clerk")
                if "AUTH0_" in content: stack.add("auth0")
                if "AWS_" in content: stack.add("aws")
            except Exception:
                pass
    return stack


SECTION_BUILDERS = {
    "supabase": section_supabase,
    "stripe":   section_stripe,
    "vercel":   section_vercel,
    "auth0":    section_auth0,
    "clerk":    section_clerk,
    "aws":      section_aws,
    "npm":      section_npm,
}


def build_playbook(stack: set[str]) -> str:
    parts = [section_intro()]
    # Order: hosting first, then auth, then payment, then cloud, then npm
    order = ["vercel", "fly", "supabase", "auth0", "clerk", "stripe", "aws", "npm"]
    for name in order:
        if name in stack and name in SECTION_BUILDERS:
            parts.append(SECTION_BUILDERS[name]())
    if not any(s in stack for s in SECTION_BUILDERS):
        parts.append("## Stack not detected\n\nNo recognised platforms found. Universal triage above still applies.\n\n---\n")
    parts.append(section_universal_close())
    return "".join(parts)


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate a stack-specific disaster recovery playbook.")
    parser.add_argument("--cwd", default=None)
    parser.add_argument("--output", default="DISASTER.md")
    parser.add_argument("--stack", default=None,
                        help="Comma-separated stack names (skip detection)")
    parser.add_argument("--print", action="store_true",
                        help="Print to stdout instead of writing a file")
    args = parser.parse_args()

    cwd = Path(args.cwd).resolve() if args.cwd else Path.cwd()
    if args.stack:
        stack = {s.strip() for s in args.stack.split(",") if s.strip()}
    else:
        stack = detect_stack(cwd)

    md = build_playbook(stack)

    if args.print:
        print(md)
        return

    out = cwd / args.output
    out.write_text(md)
    print(f"✓ Disaster playbook written: {out.relative_to(cwd) if str(out).startswith(str(cwd)) else out}")
    print(f"  Stack detected: {', '.join(sorted(stack)) or '(none)'}")
    print(f"  Bookmark it. Re-run when your stack changes.")


if __name__ == "__main__":
    main()
