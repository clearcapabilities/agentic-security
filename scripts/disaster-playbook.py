#!/usr/bin/env python3
"""
disaster-playbook.py — generate a stack-specific incident-response runbook.

Detects which platforms your project uses (Supabase, Stripe, Vercel, Auth0,
Clerk, AWS, etc.), then writes a markdown file with the EXACT commands and
URLs you'll need if you get hacked tomorrow. Stops the panic-google moment.

Usage:
  python3 scripts/disaster-playbook.py                    # auto-detect, write DISASTER.md
  python3 scripts/disaster-playbook.py --output runbook.md
  python3 scripts/disaster-playbook.py --stack supabase,stripe,vercel
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

# Each section returns markdown content. They're parameterised by detected info
# where possible (project ID, env names, etc.).

def section_intro() -> str:
    return """# 🚨 Disaster Recovery Playbook

Generated by `agentic-security`. If you're reading this BECAUSE something bad
happened, breathe, then follow the relevant section. If you're reading this
preemptively (good): bookmark this file and re-generate it whenever your
stack changes.

## The first 10 minutes — universal triage

1. **Stop the bleeding.** If you can shut down the affected service, do it:
   - Vercel: `vercel domains inspect <domain>` → `vercel rollback`
   - Fly: `fly scale count 0 --app <app>` (instant shutdown)
   - Railway: dashboard → Settings → "Pause service"
   - Cloudflare Worker: dashboard → Workers → Triggers → disable
2. **Rotate every credential** in `.env*` files (use `/rotate-key-auto --scan`).
3. **Take a snapshot** of the current DB state before doing anything else:
   ```
   pg_dump $DATABASE_URL > "evidence_$(date +%s).sql"
   ```
4. **Save your logs** before they roll off:
   - Vercel:  `vercel logs --since 24h > evidence_logs.txt`
   - Fly:     `fly logs --since 24h > evidence_logs.txt`
   - Supabase: dashboard → Logs → download
5. **Take screenshots** of everything anomalous — dashboards, error pages, weird user records.

---
"""


def section_supabase() -> str:
    return """## Supabase incident response

### Service-role key leaked

```bash
# 1. Rotate the JWT secret (invalidates ALL tokens, anon AND service-role)
# In dashboard: Settings → API → "Generate new JWT secret"

# 2. Find every server that uses the old key:
grep -rn SUPABASE_SERVICE_ROLE_KEY .

# 3. Update env vars in your deployment platform:
vercel env rm SUPABASE_SERVICE_ROLE_KEY production
vercel env add SUPABASE_SERVICE_ROLE_KEY production
# (paste new value)

# 4. Force-rotate all users' refresh tokens:
psql $DATABASE_URL -c "DELETE FROM auth.refresh_tokens;"
```

### Suspected unauthorized DB writes

```bash
# 1. Identify all table mutations in the last 24h:
psql $DATABASE_URL -c "
  SELECT schemaname, relname, n_tup_ins, n_tup_upd, n_tup_del
  FROM pg_stat_user_tables ORDER BY (n_tup_ins + n_tup_upd + n_tup_del) DESC LIMIT 20;
"

# 2. Restore from PITR (Point-in-Time Recovery) — paid plans only:
# Dashboard → Database → "Point in Time Recovery" → pick timestamp before incident

# 3. Audit your RLS policies — leaks usually start here:
psql $DATABASE_URL -c "
  SELECT schemaname, tablename, policyname, cmd, qual
  FROM pg_policies ORDER BY schemaname, tablename;
"

# 4. Force every user to re-authenticate:
psql $DATABASE_URL -c "DELETE FROM auth.refresh_tokens;"
```

### Suspected RLS bypass

If you can't tell whether RLS was bypassed:

```sql
-- Run on a SUSPECTED-COMPROMISED row to see who could have read it:
SELECT * FROM <your-table> WHERE id = '<row>'
  -- if this row returned from an anon JWT it's a bypass.

-- Audit every table's RLS:
SELECT relname, relrowsecurity, relforcerowsecurity
  FROM pg_class WHERE relkind = 'r' AND relnamespace = 'public'::regnamespace;
-- relrowsecurity = false → RLS not enabled on that table
```

### Useful URLs

- API keys & JWT secret rotation: https://supabase.com/dashboard/project/_/settings/api
- Database backups: https://supabase.com/dashboard/project/_/database/backups
- Logs: https://supabase.com/dashboard/project/_/logs

---
"""


