def section_npm() -> str:
    return """## npm supply-chain incident

### Malicious / compromised dependency detected

```bash
# 1. Find every place the package is installed (incl. transitively):
npm ls <suspect-package>

# 2. Pin it OUT via package.json "overrides":
# Edit package.json:
#   "overrides": { "<suspect-package>": "npm:noop-package@1.0.0" }
# (or pin to a known-good version)

# 3. Rebuild from scratch:
rm -rf node_modules package-lock.json
npm install

# 4. Audit install scripts in your tree (the primary supply-chain vector):
npm ls --all --parseable | xargs -I {} sh -c 'jq -r .scripts {}/package.json 2>/dev/null | grep -l "postinstall\\|preinstall\\|install" && echo "  ↑ {}"'

# 5. Rotate every credential the install scripts might have read:
#    .npmrc tokens, AWS env vars, .env files, ~/.aws/credentials
```

### Postinstall script may have exfiltrated secrets

Assume the worst:
1. Rotate ALL credentials accessible from the machine that ran `npm install`.
2. Inspect outbound network from that machine for anomalies.
3. If on CI, rotate the CI's tokens (GitHub Actions, CircleCI, etc.).

---
"""


