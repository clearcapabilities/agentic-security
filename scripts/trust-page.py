#!/usr/bin/env python3
"""
trust-page.py — generate /.well-known/security.txt and a /security page
                that displays your live security posture.

Buyers and infosec teams look for these. Most vibe-coded apps have
neither. This script writes:

  public/.well-known/security.txt          (or static/, depending on stack)
  app/security/page.tsx                    (Next.js App Router)
  src/pages/security.tsx                   (Next.js Pages Router)
  public/security/index.html               (vanilla)

Plus a JSON dump of the live posture (counts, streak, last-scan time) that
the /security page reads at build time.

Usage:
  python3 scripts/trust-page.py --contact security@myapp.com
  python3 scripts/trust-page.py --contact security@myapp.com --pgp keys.example.com/foo.asc
"""

from __future__ import annotations

import argparse
import json
import time
from datetime import datetime, timedelta, timezone
from pathlib import Path


def detect_stack(cwd: Path) -> dict:
    """Return paths to where security.txt and security page should land."""
    out = {
        "public": cwd / "public",
        "app_router": cwd / "app",
        "pages_router": cwd / "src" / "pages",
        "framework": "unknown",
    }
    pkg = cwd / "package.json"
    if pkg.exists():
        try:
            data = json.loads(pkg.read_text())
            deps = {**data.get("dependencies", {}), **data.get("devDependencies", {})}
            if "next" in deps:
                out["framework"] = "next-app" if (cwd / "app").exists() else "next-pages"
            elif "vite" in deps or "react" in deps:
                out["framework"] = "vite-react"
                out["public"] = cwd / "public"
            elif "remix" in deps or "@remix-run/node" in deps:
                out["framework"] = "remix"
                out["public"] = cwd / "public"
        except Exception:
            pass
    if (cwd / "static").exists() and not (cwd / "public").exists():
        out["public"] = cwd / "static"
    return out


def render_security_txt(contact: str, pgp_url: str | None, canonical_url: str | None) -> str:
    expires = (datetime.now(timezone.utc) + timedelta(days=365)).strftime("%Y-%m-%dT%H:%M:%S.000Z")
    lines = [
        f"Contact: mailto:{contact}",
        f"Expires: {expires}",
        "Preferred-Languages: en",
    ]
    if pgp_url:
        lines.append(f"Encryption: {pgp_url}")
    if canonical_url:
        lines.append(f"Canonical: {canonical_url}/.well-known/security.txt")
    lines.append("Policy: " + (canonical_url + "/security" if canonical_url else "https://example.com/security"))
    lines.append("")
    lines.append("# Generated by agentic-security. Re-run /trust-page to refresh the Expires date.")
    return "\n".join(lines) + "\n"


def render_posture_json(cwd: Path) -> dict:
    scan = {}
    try:
        scan = json.loads((cwd / ".agentic-security" / "last-scan.json").read_text())
    except Exception:
        pass
    streak = {}
    try:
        streak = json.loads((cwd / ".agentic-security" / "streak.json").read_text())
    except Exception:
        pass
    findings = []
    for k in ("findings", "logicVulns", "supplyChain"):
        findings.extend(scan.get(k, []))
    counts = {"critical": 0, "high": 0, "medium": 0, "low": 0}
    for f in findings:
        sev = (f.get("severity") or "low").lower()
        counts[sev] = counts.get(sev, 0) + 1
    return {
        "lastScan": scan.get("startedAt") or scan.get("scanId") or "never",
        "counts": counts,
        "streakDays": streak.get("days", 0),
        "generatedAt": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
    }


