def main() -> None:
    parser = argparse.ArgumentParser(description="Generate /.well-known/security.txt and a /security page.")
    parser.add_argument("--cwd", default=None)
    parser.add_argument("--contact", required=True, help="Security contact email")
    parser.add_argument("--pgp", default=None, help="Public PGP key URL (optional)")
    parser.add_argument("--canonical-url", default=None, help="Canonical app URL (https://yourapp.com)")
    args = parser.parse_args()

    cwd = Path(args.cwd).resolve() if args.cwd else Path.cwd()
    stack = detect_stack(cwd)

    # 1) security.txt
    pub = stack["public"]
    pub.mkdir(parents=True, exist_ok=True)
    well_known = pub / ".well-known"
    well_known.mkdir(exist_ok=True)
    sec_txt = render_security_txt(args.contact, args.pgp, args.canonical_url)
    (well_known / "security.txt").write_text(sec_txt)
    print(f"✓ Wrote: {well_known.relative_to(cwd)}/security.txt")

    # 2) Live posture JSON
    posture = render_posture_json(cwd)
    (pub / "security-posture.json").write_text(json.dumps(posture, indent=2))
    print(f"✓ Wrote: {pub.relative_to(cwd)}/security-posture.json")

    # 3) /security page tailored to framework
    if stack["framework"] == "next-app":
        page_dir = cwd / "app" / "security"
        page_dir.mkdir(parents=True, exist_ok=True)
        page = page_dir / "page.tsx"
        page.write_text(render_next_app_page(args.contact, posture))
        print(f"✓ Wrote: {page.relative_to(cwd)}")
    elif stack["framework"] == "next-pages":
        page_dir = cwd / "src" / "pages"
        page_dir.mkdir(parents=True, exist_ok=True)
        page = page_dir / "security.tsx"
        page.write_text(render_next_app_page(args.contact, posture))  # same shape works for pages router with minor tweaks
        print(f"✓ Wrote: {page.relative_to(cwd)}")
    else:
        # Vanilla HTML fallback
        sec_dir = pub / "security"
        sec_dir.mkdir(exist_ok=True)
        page = sec_dir / "index.html"
        page.write_text(render_vanilla_html(args.contact, posture))
        print(f"✓ Wrote: {page.relative_to(cwd)}")

    print()
    print(f"Framework detected:  {stack['framework']}")
    print(f"Posture snapshot:    {posture['counts'].get('critical', 0)} critical · "
          f"{posture['counts'].get('high', 0)} high · "
          f"streak={posture.get('streakDays', 0)}d")
    print()
    print("Next steps:")
    print(f"  1. Verify the URLs after deploy:")
    print(f"     - https://<yourdomain>/.well-known/security.txt")
    print(f"     - https://<yourdomain>/security")
    print(f"  2. Re-run /trust-page periodically — security.txt has a 1-year expiry.")
    print(f"  3. Run /security-onepager to generate the sales-deliverable counterpart.")


if __name__ == "__main__":
    main()
